#Tipos de datos entero o largo
numInt = 6
print(numInt)
print(numInt, type(numInt))

#Tipos de datos Flotantes
numFloat = 8.23
print(numFloat, type(numFloat))

#Tipos de datos Complejos
numComplex = 5 + 5j
print(numComplex, type(numComplex))

